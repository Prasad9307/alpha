public class Logicaloperator {
    public static void main(String[] args) {
        System.out.println( (3>2) && (05>0)); //only two statement is true answer is true (Logical AND)

        System.out.println( (1>2) && (05>10)); //only two false statment so answer is false order time true (Logical OR)

        System.out.println(!(4<10));  //one statement is false, so the answer will be true! (LOgical NOT)
    }
}
