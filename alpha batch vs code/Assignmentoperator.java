public class Assignmentoperator {
    public static void main(String[] args) {
        int A = 10;
        // A = A + 10;
        A += 10;
        System.out.println(A);

        int B = 20;
        // B = B-10;
        B -= 10;
        System.out.println(B);

        int C = 5;
        // C = C * 5;
        C *= 5;
        System.out.println(C);

        int D = 25;
        // D =  D / 5;
        D /= 5;
        System.out.println(D);

        int E = 26;
        // E = E % 5;
        E %= 5;
        System.out.println(E);


    }
    
}
