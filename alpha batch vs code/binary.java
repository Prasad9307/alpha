public class binary {
    public static void main(String[] args) {
        int a = 12;
        int b = 5;
        System.out.println("Add two number: " + (a+b));
        System.out.println("Subtract : " + (a-b));
        System.out.println("Multiply : " + (a*b));
        System.out.println("Divide : " + (a/b));
        System.out.println("Module : " + (a%b));
    }
    
}
