public class swap {
    public static void main(String args[]){
        
        //Swap - exchange a value
        int a = 10;
        int b = 5;

        //swap
        int temp = a;
        a = b ;
        b = temp;

        System.out.println("Value of a = " +a);
        System.out.println("Value of b = " +b);

    }
    
}
